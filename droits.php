					<div class="col-lg-12">
						<blockquote class="generic-blockquote">
							<p>— Tout enfant a le droit de se reposer et d’avoir des loisirs propres à son âge.</p>
							<p>— Tout enfant a le droit de participer pleinement à la vie culturelle et artistique. Pour cela, les Etats parties encouragent la mise en place de moyens appropriés, dans des conditions d’égalité.</p>
						</blockquote>
					</div>
				</div>

				<h3 class="mb-30" id="article32" style="color: #BD0101">Article 32</h3>
				<div class="row">
					<div class="col-lg-12">
						<blockquote class="generic-blockquote">
							<p>— L’enfant doit être protégé contre l’exploitation économique et ne doit être contraint à aucun travail comportant des risques pour sa santé, son éducation et son développement physique, mental, spirituel, moral ou social.</p>
							<p>— Les Etats parties prennent des mesures :</p>
							<p>- Ils fixent un âge minimum pour travailler.</p>
							<p>- Ils prévoient des horaires et des conditions de travail appropriées.</p>
							<p>- Ils prévoient des sanctions pour assurer l’application de cet article.</p>
						</blockquote>
					</div>
				</div>

				<h3 class="mb-30" id="article33" style="color: #BD0101">Article 33</h3>
				<div class="row">
					<div class="col-lg-12">
						<blockquote class="generic-blockquote">
							<p>Les Etats parties prennent toutes les mesures pour protéger les enfants contre l’usage de drogues et pour empêcher que les enfants ne soient utilisés pour la production et le trafic illégal de ces substances.</p>
						</blockquote>
					</div>
				</div>

