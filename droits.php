				<div class="row">
					<div class="col-lg-12">
						<blockquote class="generic-blockquote">
							<p>Tout enfant a le droit d’avoir sa propre vie culturelle, de professer et de pratiquer sa propre religion et d’employer sa propre langue.</p>
						</blockquote>
					</div>
				</div>

