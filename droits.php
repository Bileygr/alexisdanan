				<div class="row">
					<div class="col-lg-12">
						<blockquote class="generic-blockquote">
							<p>Les Etats parties protègent l’enfant contre toutes les autres formes d’exploitation qui nuiraient à son bien-être.</p>
						</blockquote>
					</div>
				</div>

				<h3 class="mb-30" id="article37" style="color: #BD0101">Article 37</h3>
				<div class="row">
					<div class="col-lg-12">
						<blockquote class="generic-blockquote">
							<p>Les Etats parties veillent à ce que :</p>
							<p>- Aucun enfant ne soit soumis à la torture, à des peines ou des traitements cruels, inhumains ou dégradants.</p>
							<p>- Aucun enfant ne soit privé de liberté de façon illégale.</p>
							<p>- Tout enfant privé de liberté soit traité avec humanité et avec le respect dû à la dignité de la personne humaine en tenant compte des besoins des personnes de son âge.</p>
							<p>- Les enfants privés de liberté ont le droit d’avoir rapidement accès à toute aide appropriée.</p>
						</blockquote>
					</div>
				</div>

				<h3 class="mb-30" id="article38" style="color: #BD0101">Article 38</h3>
				<div class="row">
					<div class="col-lg-12">
						<blockquote class="generic-blockquote">
							<p>— En cas de conflit armé, les Etats parties doivent respecter les règles du droit humanitaire international.</p>
							<p>— Les Etats parties doivent veiller à ce que les enfants de moins de 15 ans ne participent pas aux combats.</p>
							<p>— Les Etats parties doivent s’abstenir d’enrôler les enfants de moins de 15 ans dans leurs forces armées. Entre 15 et 18 ans, ils doivent s’efforcer de d’abord enrôler les plus âgés.</p>
							<p>— Les Etats parties doivent prendre toutes les mesures pour que les enfants touchés par un conflit armé bénéficient d’une protection et de soins.</p>
						</blockquote>
					</div>
				</div>

