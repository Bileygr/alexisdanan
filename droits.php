				<div class="row">
					<div class="col-lg-12">
						<blockquote class="generic-blockquote">
							<p>Les Etats parties prennent toutes les mesures pour faciliter la réadaptation physique et psychologique et la réinsertion sociale de tout enfant victime de toute forme de négligence, d’exploitation ou de sévices, de torture ou de toute autre forme de peines ou traitements cruels, inhumains ou dégradants, ou de conflit armé.</p>
						</blockquote>
					</div>
				</div>

				<h3 class="mb-30" id="article40" style="color: #BD0101">Article 40</h3>
				<div class="row">
					<div class="col-lg-12">
						<blockquote class="generic-blockquote">
							<p>— Tout enfant suspecté ou accusé d’infraction à la loi pénale a droit à un traitement qui tiendra compte de son âge et qui lui apprendra à respecter les droits de l’homme et les libertés fondamentales de chacun, et à assumer un rôle dans la société.</p>
							<p>— Tout enfant suspecté ou accusé d’infraction à la loi pénale a au minimum droit :</p>
							<p>- À être présumé innocent jusqu’à ce que sa culpabilité soit prouvée.</p>
							<p>- À être rapidement informé de ce qui lui est reproché et à bénéficier d’une aide appropriée pour assurer sa défense.</p>
							<p>- À ce que sa cause soit rapidement entendue par les autorités compétentes.</p>
							<p>- À ne pas être contraint de témoigner ou de s’avouer coupable et à ce que les témoins soient entendus.</p>
							<p>- À faire appel de la décision s’il est reconnu coupable.</p>
							<p>- À être gratuitement assisté d’un interprète s’il ne parle pas la langue utilisée.</p>
							<p>- À ce que sa vie privée soit pleinement respectée.</p>
							<p>— Les Etats parties s’efforcent de faire adopter des lois spécialement conçues pour les enfants suspectés ou accusés d’infraction à la loi pénale et :</p>
							<p>- D’établir un âge en dessous duquel les enfants sont considérés incapables d’enfreindre la loi pénale.</p>
							<p>- De ne pas recourir à la procédure judiciaire chaque fois que cela est possible et souhaitable pour l’enfant.</p>
							<p>— Des dispositions sont prises pour assurer aux enfants un traitement conforme à leur bien-être et proportionné à leur situation et à l’infraction.</p>
						</blockquote>
					</div>
				</div>

