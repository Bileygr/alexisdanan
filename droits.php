						<blockquote class="generic-blockquote">
							<p>Les Etats parties s’engagent à protéger l’enfant contre toutes les formes d’exploitation sexuelle et de violence sexuelle.</p>
						</blockquote>
					</div>
				</div>

				<h3 class="mb-30" id="article35" style="color: #BD0101">Article 35</h3>
				<div class="row">
					<div class="col-lg-12">
						<blockquote class="generic-blockquote">
							<p>Les Etats parties prennent toutes les mesures pour empêcher l’enlèvement, la vente ou la traite d’enfants.</p>
						</blockquote>
					</div>
				</div>

