					<div class="row">
						<div class="col-lg-12">
							<blockquote class="generic-blockquote">
								<p>Tout être humain âgé de moins de 18 ans est un enfant.</p>
							</blockquote>
						</div>
					</div>

					<h3 class="mb-30" id="article2" style="color: #BD0101">Article 2</h3>
					<div class="row">
						<div class="col-lg-12">
							<blockquote class="generic-blockquote">
								<p>— Les Etats parties doivent respecter les droits de l’enfant sans distinction de sexe, de couleur, de langue, de religion, d’opinion…</p>
								<p>— Tous les enfants doivent être protégés de toutes les formes de discrimination.</p>
							</blockquote>
						</div>
					</div>

					<h3 class="mb-30" id="article3" style="color: #BD0101">Article 3</h3>
					<div class="row">
						<div class="col-lg-12">
							<blockquote class="generic-blockquote">
								<p>— Toute décision prise doit d’abord tenir compte de l’intérêt de l’enfant.</p>
								<p>— Il faut s’assurer du bien-être de l’enfant.</p>
								<p>— Tous les établissements qui s’occupent d’enfants doivent respecter les lois du pays.</p>
							</blockquote>
						</div>
					</div>

					<h3 class="mb-30" id="article4" style="color: #BD0101">Article 4</h3>
					<div class="row">
						<div class="col-lg-12">
							<blockquote class="generic-blockquote">
								<p>Les pays qui ont signé la Convention doivent tout faire pour respecter les droits de l’enfant.</p>
							</blockquote>
						</div>
					</div>

					<h3 class="mb-30" id="article5" style="color: #BD0101">Article 5</h3>
					<div class="row">
						<div class="col-lg-12">
							<blockquote class="generic-blockquote">
								<p>Ce sont d’abord les parents, ou toute personne responsable de l’enfant, qui doivent respecter les droits de l’enfant.</p>
							</blockquote>
						</div>
					</div>

					<h3 class="mb-30" id="article6" style="color: #BD0101">Article 6</h3>
					<div class="row">
						<div class="col-lg-12">
							<blockquote class="generic-blockquote">
								<p>— Tous les enfants ont le droit de vivre.</p>
								<p>— Les Etats parties doivent tout faire pour que l’enfant vive et se développe.</p>
							</blockquote>
						</div>
					</div>

					<h3 class="mb-30" id="article7" style="color: #BD0101">Article 7</h3>
					<div class="row">
						<div class="col-lg-12">
							<blockquote class="generic-blockquote">
								<p>— Dès qu’il naît, un enfant doit être enregistré, avoir un nom et être élevé, si possible, par ses parents.</p>
								<p>— Les pays, selon leurs lois, doivent s’assurer que ces droits sont respectés.</p>
							</blockquote>
						</div>
					</div>

					<h3 class="mb-30" id="article8" style="color: #BD0101">Article 8</h3>
					<div class="row">
						<div class="col-lg-12">
							<blockquote class="generic-blockquote">
								<p>— Les Etats parties s’engagent à respecter l’identité de l’enfant, c’est-à-dire sa nationalité, son nom et ses relations familiales.</p>
								<p>— Si un enfant est privé d’un élément de son identité (sa nationalité, ou son nom, ou ses relations familiales), c’est illégal. L’enfant doit être protégé et aidé pour que son identité soit rétablie.</p>
							</blockquote>
						</div>
					</div>

					<h3 class="mb-30" id="article9" style="color: #BD0101">Article 9</h3>
					<div class="row">
						<div class="col-lg-12">
							<blockquote class="generic-blockquote">
								<p>— L’enfant ne doit pas être séparé de ses parents, sauf si la loi le décide pour son bien.</p>
								<p>— Quand les autorités décident de séparer l’enfant de ses parents, chacun a le droit de faire connaître son point de vue.</p>
								<p>— L’enfant séparé de ses parents a le droit de les voir régulièrement, sauf si c’est contre son intérêt.</p>
								<p>— Quand la séparation est décidée par l’Etat, il faut dire à l’enfant où se trouvent son ou ses parents, sauf si c’est contre son intérêt.</p>
							</blockquote>
						</div>
					</div>

					<h3 class="mb-30" id="article10" style="color: #BD0101">Article 10</h3>
					<div class="row">
						<div class="col-lg-12">
							<blockquote class="generic-blockquote">
								<p>— Un enfant ou ses parents peuvent demander à quitter leur pays ou à entrer dans un autre pour réunir leur famille.</p>
								<p>— Un enfant, dont les parents habitent dans un autre pays, a le droit de quitter son pays et d’y revenir pour les voir régulièrement.</p>
							</blockquote>
						</div>
					</div>

					<h3 class="mb-30" id="article11" style="color: #BD0101">Article 11</h3>
					<div class="row">
						<div class="col-lg-12">
							<blockquote class="generic-blockquote">
								<p>Les Etats parties luttent pour que les enfants ne soient pas emmenés illégalement à l’étranger.</p>
							</blockquote>
						</div>
					</div>

					<h3 class="mb-30" id="article12" style="color: #BD0101">Article 12</h3>
					<div class="row">
						<div class="col-lg-12">
							<blockquote class="generic-blockquote">
								<p>— Tout enfant en âge de comprendre la situation a le droit de donner librement son avis sur les questions le concernant.</p>
								<p>— Dans ce cas, l’enfant a la possibilité d’être entendu (directement ou par l’intermédiaire d’un adulte) dans toute procédure judiciaire ou administrative qui le concerne.</p>
							</blockquote>
						</div>
					</div>

					<h3 class="mb-30" id="article13" style="color: #BD0101">Article 13</h3>
					<div class="row">
						<div class="col-lg-12">
							<blockquote class="generic-blockquote">
								<p>— L’enfant a droit à la liberté d’expression : il est libre de rechercher, recevoir et répandre des informations et des idées par le moyen de son choix (parole, écriture, art…).</p>
								<p>— Ce droit a des limites :
									<br>&nbsp;- il faut respecter les droits et la réputation des autres,
									<br>&nbsp;- il faut sauvegarder la sécurité nationale, l’ordre public, la santé et la moralité publiques.</p>
							</blockquote>
						</div>
					</div>

					<h3 class="mb-30" id="article14" style="color: #BD0101">Article 14</h3>
					<div class="row">
						<div class="col-lg-12">
							<blockquote class="generic-blockquote">
								<p>— Les Etats parties doivent respecter le droit de l’enfant à être libre de penser ce qu’il veut et d’avoir la religion qu’il veut.</p>
								<p>— Les Etats parties respectent le droit et le devoir des parents, ou des représentants légaux, de guider l’enfant pour qu’il développe ses capacités de penser..</p>
								<p>— La liberté de manifester sa religion ou ses convictions est soumise aux restrictions de la loi du pays. Cette liberté d’expression doit se faire dans le respect des autres.</p>
							</blockquote>
						</div>
					</div>

					<h3 class="mb-30" id="article15" style="color: #BD0101">Article 15</h3>
					<div class="row">
						<div class="col-lg-12">
							<blockquote class="generic-blockquote">
								<p>— Les enfants ont droit à la liberté d’association et à la liberté de réunion pacifique.</p>
								<p>— Il faut respecter les limites imposées par la loi du pays.</p>
							</blockquote>
						</div>
					</div>

					<h3 class="mb-30" id="article16" style="color: #BD0101">Article 16</h3>
					<div class="row">
						<div class="col-lg-12">
							<blockquote class="generic-blockquote">
								<p>— Il est interdit de s’introduire illégalement dans la vie privée d’un enfant et de nuire à son honneur et sa réputation.</p>
								<p>— L’enfant doit être protégé par la loi de telles atteintes.</p>
							</blockquote>
						</div>
					</div>

					<h3 class="mb-30" id="article17" style="color: #BD0101">Article 17</h3>
					<div class="row">
						<div class="col-lg-12">
							<blockquote class="generic-blockquote">
								<p>Les médias sont importants. Ils doivent permettre aux enfants d’avoir accès à des informations nationales et internationales adaptées à leur âge.</p>
							</blockquote>
						</div>
					</div>

					<h3 class="mb-30" id="article18" style="color: #BD0101">Article 18</h3>
					<div class="row">
						<div class="col-lg-12">
							<blockquote class="generic-blockquote">
								<p>— La responsabilité d’élever un enfant et d’assurer son développement revient d’abord à ses deux parents. Si ce n’est pas possible, elle revient à ses représentants légaux.</p>
								<p>— Les Etats parties doivent aider les parents, ou les représentants légaux, et mettre en place des établissements et des services chargés de veiller au bien-être des enfants.</p>
								<p>— Les Etats parties doivent prendre des mesures pour la garde des enfants lorsque les parents travaillent.</p>
							</blockquote>
						</div>
					</div>

					<h3 class="mb-30" id="article19" style="color: #BD0101">Article 19</h3>
					<div class="row">
						<div class="col-lg-12">
							<blockquote class="generic-blockquote">
								<p>Les Etats parties doivent prendre toutes les mesures nécessaires pour protéger l’enfant contre toutes les formes de violence (physiques et morales).</p>
							</blockquote>
						</div>
					</div>

					<h3 class="mb-30" id="article20" style="color: #BD0101">Article 20</h3>
					<div class="row">
						<div class="col-lg-12">
							<blockquote class="generic-blockquote">
								<p>— Tout enfant, temporairement ou définitivement, séparé de sa famille a droit à une protection et une aide spéciales de l’Etat.</p>
								<p>— La protection et l’aide de l’Etat doivent tenir compte des lois du pays.</p>
								<p>— La protection apportée doit être en continuité avec l’éducation déjà donnée à l’enfant. Il faut tenir compte de son origine, sa religion, sa culture et sa langue.</p>
							</blockquote>
						</div>
					</div>

					<h3 class="mb-30" id="article21" style="color: #BD0101">Article 21</h3>
					<div class="row">
						<div class="col-lg-12">
							<blockquote class="generic-blockquote">
								<p>En cas d’adoption, les Etats parties doivent s’assurer que l’intérêt de l’enfant est la première préoccupation.</p>
							</blockquote>
						</div>
					</div>

					<h3 class="mb-30" id="article22" style="color: #BD0101">Article 22</h3>
					<div class="row">
						<div class="col-lg-12">
							<blockquote class="generic-blockquote">
								<p>— Tout enfant qui cherche à obtenir le statut de réfugié (ou qui est considéré comme réfugié dans le pays où il se trouve) doit bénéficier de la protection et de l’assistance humanitaire.</p>
								<p>— Les Etats parties doivent travailler avec l’Organisation des Nations Unies (ONU) pour aider l’enfant à retrouver sa famille s’il en a été séparé.
								<br>&nbsp;Lorsque c’est impossible, l’enfant réfugié a droit à la même protection que tout autre enfant privé de son milieu familial (article 20).</p>
							</blockquote>
						</div>
					</div>

					<h3 class="mb-30" id="article23" style="color: #BD0101">Article 23</h3>
					<div class="row">
						<div class="col-lg-12">
							<blockquote class="generic-blockquote">
								<p>— Les enfants souffrant d’un handicap physique ou mental doivent mener une vie pleine et décente. Ils ont les mêmes droits que les autres enfants.</p>
								<p>— Les enfants handicapés ont droit à des soins spéciaux et une aide adaptée à leur état.</p>
								<p>— L’aide fournie est gratuite, si c’est possible. Elle doit permettre aux enfants handicapés d’avoir accès à l’éducation, aux soins de santé, à la rééducation, aux loisirs…</p>
								<p>— Les Etats parties doivent s’échanger les informations qu’ils ont sur les soins à donner aux enfants handicapés.</p>
							</blockquote>
						</div>
					</div>

					<h3 class="mb-30" id="article24" style="color: #BD0101">Article 24</h3>
					<div class="row">
						<div class="col-lg-12">
							<blockquote class="generic-blockquote">
								<p>— Tous les enfants ont droit au meilleur état de santé possible. Ils ont le droit de bénéficier des services médicaux et de rééducation.</p>
								<p>— Les Etats parties s’efforcent d’assurer la réalisation de ce droit et d’informer la population sur l’importance de la nourriture, de l’hygiène…pour la santé des enfants.</p>
								<p>— Les Etats parties prennent des mesures pour arrêter les pratiques traditionnelles dangereuses pour la santé des enfants.</p>
								<p>— Les Etats parties doivent travailler ensemble pour permettre la totale réalisation de cet article.</p>
							</blockquote>
						</div>
					</div>
				</div>

				<h3 class="mb-30" id="article25" style="color: #BD0101">Article 25</h3>
				<div class="row">
					<div class="col-lg-12">
						<blockquote class="generic-blockquote">
							<p>Tout enfant placé dans un établissement pour recevoir des soins, une protection ou un traitement (physique ou mental) a le droit d’avoir un examen régulier pour s’assurer que son placement est nécessaire.</p>
						</blockquote>
					</div>
				</div>

				<h3 class="mb-30" id="article26" style="color: #BD0101">Article 26</h3>
				<div class="row">
					<div class="col-lg-12">
						<blockquote class="generic-blockquote">
							<p>Tout enfant a le droit de bénéficier de la sécurité sociale et des assurances sociales (en accord avec les lois du pays).</p>
						</blockquote>
					</div>
				</div>

				<h3 class="mb-30" id="article27" style="color: #BD0101">Article 27</h3>
				<div class="row">
					<div class="col-lg-12">
						<blockquote class="generic-blockquote">
							<p>— Tout enfant a droit à un niveau de vie suffisant pour lui permettre de bien se développer.</p>
							<p>— Ce sont d’abord les parents, ou toute personne ayant la charge de l’enfant, qui ont la responsabilité de faire de leur mieux pour assurer les conditions de vie nécessaires au développement de l’enfant.</p>
							<p>— Les Etats parties doivent, selon leurs moyens, aider les parents, ou toute personne ayant la charge de l’enfant, quand ils en ont besoin.</p>
						</blockquote>
					</div>
				</div>

				<h3 class="mb-30" id="article28" style="color: #BD0101">Article 28</h3>
				<div class="row">
					<div class="col-lg-12">
						<blockquote class="generic-blockquote">
							<p>— Tous les enfants ont droit à l’éducation. Pour cela, l’école primaire est obligatoire et gratuite pour tous.</p>
							<p>— Les Etats parties veillent à ce qu’à l’école la dignité de l’enfant soit respectée.</p>
							<p>— Les Etats parties encouragent la coopération internationale dans le domaine de l’éducation pour aider à éliminer l’ignorance et l’analphabétisme dans le monde.</p>
						</blockquote>
					</div>
				</div>

				<h3 class="mb-30" id="article29" style="color: #BD0101">Article 29</h3>
				<div class="row">
					<div class="col-lg-12">
						<blockquote class="generic-blockquote">
							<p>L’éducation de l’enfant doit :</p>
							<p>- Favoriser l’épanouissement de la personnalité de l’enfant et le développement de ses dons et aptitudes mentales et physiques.</p>
							<p>- Inculquer à l’enfant le respect des droits de l’homme et des libertés fondamentales.</p>
							<p>- Inculquer à l’enfant le respect de ses parents, de ses valeurs culturelles ainsi que le respect des valeurs des autres.</p>
							<p>- Préparer l’enfant à assumer ses responsabilités dans un esprit de paix, de tolérance, d’égalité entre les sexes et d’amitié entre tous les peuples.</p>
							<p>- Inculquer le respect du milieu naturel.</p>
						</blockquote>
					</div>
				</div>

